package myGL;

// Different from color in class Pixel. The latter one is short type, for writing to file. This one is for calculating.
public class Color
{
	public float red;
	public float green;
	public float blue;
	
	public Color()
	{
		
	}

	public Color(float red, float green, float blue)
	{
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
}
